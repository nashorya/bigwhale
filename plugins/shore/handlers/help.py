help_cmd = on_command("帮助", priority=5, block=True)


@help_cmd.handle()
async def handle_help(
    bot: Bot,
    event: PrivateMessageEvent,
    matcher: Matcher,
    args: Message = CommandArg(),
):
    """处理 #帮助 和 #帮助 模块名"""
    # 仅处理私聊
    raw = args.extract_plain_text().strip()

    if not raw:
        # 无参数 → 显示一级帮助菜单
        await matcher.finish(HELP_MAIN)

    # 有参数 → 查找对应模块详情
    module_key = MODULE_ALIASES.get(raw)

    if module_key and module_key in HELP_MODULES:
        await matcher.finish(HELP_MODULES[module_key])
    else:
        # 未找到匹配的模块
        available = "、".join(HELP_MODULES.keys())
        await matcher.finish(
            f"未找到「{raw}」相关帮助。\n"
            f"\n"
            f"可用模块：{available}\n"
            f"输入 #帮助 + 模块名 查看详情"
        )


# ──────────────────────────────────────────────
# 未识别消息引导（最低优先级）
# ──────────────────────────────────────────────

# priority=99 保证在所有指令和情绪监听之后执行
# block=False 不阻断其他可能的处理器
unrecognized_guide = on_message(priority=99, block=False)


@unrecognized_guide.handle()
async def handle_unrecognized(
    bot: Bot, event: PrivateMessageEvent, matcher: Matcher
):
    """
    当用户发了非指令的普通消息，且未被其他 handler 处理时，
    温馨提示用户可用指令。每个用户每小时最多提示 1 次。
    """
    text = event.get_plaintext().strip()

    # 跳过空消息
    if not text:
        return

    # 跳过指令消息（# 开头的已被 on_command 处理）
    if text.startswith("#"):
        return

    # 跳过非常短的消息（可能是表情、"嗯"之类的）
    if len(text) <= 1:
        return

    uid = str(event.user_id)
    now = time.time()

    # 冷却检查：同一用户 1 小时内只提醒 1 次
    last_time = _last_guide_time.get(uid, 0)
    if now - last_time < GUIDE_COOLDOWN:
        return

    _last_guide_time[uid] = now

    guide_msg = (
        "💡 小提示：我是通过指令来工作的哦！\n"
        "\n"
        "所有指令以 # 开头，例如：\n"
        "  #帮助     查看所有指令\n"
        "  #今日计划  查看学习安排\n"
        "  #打卡     完成知识点打卡\n"
        "  #陪我聊   想聊天也可以\n"
        "\n"
        "发送 #帮助 查看完整指令列表"
    )
    await bot.send(event, guide_msg)

